    public void setCliente(Cliente cliente){
        this.cliente = cliente;
        jTextCedula.setText(String.valueOf(cliente.getCedula()));
        jTextNombre.setText(cliente.getNombre());
        jTextTelefono.setText(String.valueOf(cliente.getTelefono()));
        
    }
    
    private void JbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbActualizarActionPerformed
        // TODO add your handling code here:
        ClienteService clienteService = new ClienteService();
        
        //variables para gaurdar los datos de JText
        String nombre = jTextNombre.getText();         
        int cedula = Integer.parseInt(jTextCedula.getText());
        int telefono = Integer.parseInt(jTextTelefono.getText());
        
        //Constructor con los valores obtenidos                       
        Cliente cliente = new Cliente(cedula, nombre, telefono);
        
        
        System.out.println("Datos obteneidos");
        System.out.println("nombre:" + nombre);
        System.out.println("cedula:" + cedula);
        System.out.println("telefono:" + telefono);
        //Envia los datos obtenidos a la funcion create
        clienteService.update(cliente);
        
        //Mostrar mensage de confirmaci√≥n
        JOptionPane.showMessageDialog(rootPane, "Actualizado exitozamente.\n" + cliente);
        
        //Liampiar los jText para poder ingresar nuevos datos
        
        uiMenuCliente.setVisible(true);
        this.setVisible(false);
        
    }//GEN-LAST:event_JbActualizarActionPerformed

